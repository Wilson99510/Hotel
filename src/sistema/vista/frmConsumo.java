    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if (txtIdproducto.getText().length()== 0){
            JOptionPane.showConfirmDialog(rootPane, "Ingresar un Producto");
            btnBuscaproductos.requestFocus();
            return;
        }

        if (txtCantidad.getText().length()== 0){
            JOptionPane.showConfirmDialog(rootPane, "Ingresar una Cantidad de Consumo");
            txtCantidad.requestFocus();
            return;
        }

        if (txtPrecio_venta.getText().length()== 0){
            JOptionPane.showConfirmDialog(rootPane, "Ingresar Precio de Venta del Producto");
            txtPrecio_venta.requestFocus();
            return;
        }

        consumo dts = new consumo();              //instanciamos la clase consumo para crear un nuevo objeto
        ctlconsumo funcion = new ctlconsumo();    //instanciamos la clase ctlconsumo para llamar a todas las funciones

        //empezamos a enviar los datos
        dts.setIdreserva(Integer.parseInt(txtIdreserva.getText()));
        dts.setIdproducto(Integer.parseInt(txtIdproducto.getText()));
        dts.setCantidad(Double.parseDouble(txtCantidad.getText()));
        dts.setPrecio_venta(Double.parseDouble(txtPrecio_venta.getText())); //se le agrega el Double ya que espera un valor numerico
        //para enviar de un combo box, hay que seleccionar un indice y se declara una variable de tipo int llamado "seleccionado"
        int seleccionado=cboEstado.getSelectedIndex();
        dts.setEstado((String) cboEstado.getItemAt(seleccionado));

        if  (accion.equals("guardar")){     //usamos el equals para comparar cadena de caracteres
            if(funcion.insertar(dts)){
                JOptionPane.showMessageDialog(rootPane,"El Consumo " + txtProducto.getText() + " Del Cliente"
                        + txtCliente.getText() + " Ha sido Registrado Exitosamente");
                        
                mostrar(idreserva);
                deshabilitar();
            }
        }else if (accion.equals("modificar")){      //determinamos si la accion es modificar
            dts.setIdconsumo(Integer.parseInt(txtIdconsumo.getText())); //Se convierte a entero por la incompatibilidad de variables
            if (funcion.modificar(dts)){
                JOptionPane.showMessageDialog(rootPane,"El Consumo del Cliente "
                        + txtCliente.getText() + " Ha sido Modificado Exitosamente");
                mostrar(idreserva); //para no mostrar todos los consumos sino el consumo de la reserva en espec√≠fica
                deshabilitar();
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

