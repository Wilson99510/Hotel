    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if (txtNombre.getText().length()== 0){
            JOptionPane.showConfirmDialog(rootPane, "Ingresar Nombre del Cliente");
            txtNombre.requestFocus();
            return;
        }

        if (txtApellido.getText().length()== 0){
            JOptionPane.showConfirmDialog(rootPane, "Ingresar Apellido del Cliente");
            txtApellido.requestFocus();
            return;
        }

        if (txtNumero_documento.getText().length()== 0){
            JOptionPane.showConfirmDialog(rootPane, "Ingresar Numero de Documento del Cliente");
            txtNumero_documento.requestFocus();
            return;
        }
        
        if (txtCodigo_cliente.getText().length()== 0){
            JOptionPane.showConfirmDialog(rootPane, "Ingresar Código del Cliente");
            txtCodigo_cliente.requestFocus();
            return;
        }

        cliente dts = new cliente();  //instanciamos la clase cliente para crear un nuevo objeto
        ctlcliente funcion = new ctlcliente();    //instanciamos la clase ctlcliente para llamar a todas las funciones

        //empezamos a enviar los datos
        dts.setNombre(txtNombre.getText());
        dts.setApellido(txtApellido.getText());
        
        //para enviar de un combo box, hay que seleccionar un indice y se declara una variable de tipo int llamado "seleccionado"
        int seleccionado=cboTipo_documento.getSelectedIndex();
        dts.setTipo_documento((String) cboTipo_documento.getItemAt(seleccionado));

        dts.setNumero_documento((txtNumero_documento.getText())); 
        dts.setDireccion(txtDireccion.getText()); 
        dts.setTelefono(txtTelefono.getText());
        dts.setCorreo(txtCorreo.getText()); 
        dts.setCodigo_cliente(txtCodigo_cliente.getText()); 

        if  (accion.equals("guardar")){     //usuamos el equals para comparar cadena de caracteres
            if(funcion.insertar(dts)){
                JOptionPane.showMessageDialog(rootPane,"Registro Exitoso");
                mostrar("");
                deshabilitar();
            }
        }else if (accion.equals("modificar")){      //determinamos si la accion es modificar
            dts.setIdpersona(Integer.parseInt(txtIdpersona.getText())); //Se convierte a entero por la incompatibilidad de varibles
            if (funcion.modificar(dts)){
                JOptionPane.showMessageDialog(rootPane,"Modificación Exitosa");
                mostrar("");
                deshabilitar();
            }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

