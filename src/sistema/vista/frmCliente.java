    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        if(!txtIdpersona.getText().equals("")) { // si no es igual a (espacio en blanco) se procede a eliminar el cliente
            int confirmacion = JOptionPane.showConfirmDialog(rootPane, "Desea Eliminar el Cliente?","Confirmar",2);

            if(confirmacion==0){
                ctlcliente funcion = new ctlcliente();
                cliente dts= new cliente();
                dts.setIdpersona(Integer.parseInt(txtIdpersona.getText()));
                funcion.eliminar(dts);
                mostrar("");
                deshabilitar(); //para desactivar todas las cajas
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

